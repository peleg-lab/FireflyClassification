def parse_inputs(dates, ratios):
    date_ratio_zip = list(zip(dates.split(','), [float(x) for x in ratios.split(',')]))
    involved_dates = [x for x in date_ratio_zip if x[1] > 0]
    s = involved_dates[0][0][-2:] + str(involved_dates[0][1]) + involved_dates[1][0][-2:] + str(involved_dates[1][1])
    return s


def get_retval(cm, sp):
    sp_short = sp[:2]
    species_dict = {'bw': 0,
                    'ub': 1,
                    'ic': 2,
                    'ur': 3,
                    'uf': 4,
                    'ik': 5,
                    'io': 6}
    species_val = species_dict[sp_short]
    if cm[species_val, species_val] >= 0.5:
        retval = (sp_short, 1)
    else:
        retval = (sp_short, 0)
    return retval


def parse_cm(cm, species_pair):
    species_1 = species_pair[:int(len(species_pair) / 2)]
    species_2 = species_pair[int(len(species_pair) / 2):]
    species_1_retval = get_retval(cm, species_1)
    species_2_retval = get_retval(cm, species_2)
    return species_1_retval, species_2_retval


